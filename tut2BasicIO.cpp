#include <iostream>
using namespace std;

int main()
{
    int a,b;
    cout<<"Enter the value a:\n"; /* '<<' is Known as  Insertion operation */
    cin>>a; /* '>>' is Known as  Extraction operation */

    cout<<"Enter the value b:\n"; /* '<<' is Known as  Insertion operation */
    cin>>b; /* '>>' is Known as  Extraction operation */

    cout<<"Sum :\n"<<a+b;
    return 0;
}
